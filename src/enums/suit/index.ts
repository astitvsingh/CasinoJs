/**
 * @enum `Suit`
 * Represents the suits of playing cards used in poker.
 * Each suit corresponds to one of the four card suits: Hearts, Diamonds, Clubs, and Spades.
 *
 * @example
 * const suit = Suit.Hearts;
 * console.log(suit); // "Hearts"
 */
enum Suit {
  Hearts = "Hearts",
  Diamonds = "Diamonds",
  Clubs = "Clubs",
  Spades = "Spades",
}

export { Suit };
